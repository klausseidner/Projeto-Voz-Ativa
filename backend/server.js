///////////////////////////////////////////////////////////////////////////////////////////////////////
// Arquivo principal da Aplicação
///////////////////////////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Importando as dependências necessárias
///////////////////////////////////////////////////////////////////////////////////////////////////////
const express = require('express'); // Framework Express.js
const cors = require('cors'); // Middleware para permitir cross-origin resource sharing (CORS)
const bodyParser = require('body-parser'); // Middleware para parsear o corpo da requisição
const { initializeDb } = require('./config/db'); // Função para inicializar o banco de dados
const authRoutes = require('./routes/authRoutes'); // Rotas para o login
const userRoutes = require('./routes/userRoutes'); // Rotas para os usuários
const incidentRoutes = require('./routes/incidentRoutes'); // Rotas para os incidentes
const secretaryRoutes = require('./routes/secretaryRoutes'); // Rotas para as secretárias
require('dotenv').config(); // Carrega as variáveis de ambiente do arquivo.env
const app = express(); // Inicializa o Express.js

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Usando a porta definida no .env ou a porta padrão 3000
///////////////////////////////////////////////////////////////////////////////////////////////////////
const PORT = process.env.PORT || 3000;

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Middleware
///////////////////////////////////////////////////////////////////////////////////////////////////////
app.use(cors()); // Para permitir cross-origin resource sharing (CORS)
app.use(bodyParser.json()); // Para analisar o corpo da requisição em JSON
app.use(express.json()); // Para analisar o corpo da requisição em JSON

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Inicializa o banco de dados
///////////////////////////////////////////////////////////////////////////////////////////////////////
initializeDb(); // Inicializa o banco de dados

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Definindo as rotas
///////////////////////////////////////////////////////////////////////////////////////////////////////
app.use('/api/auth', authRoutes); // Rotas para o login
app.use('/api/users', userRoutes); // Rotas para os usuários
app.use('/api/incidents', incidentRoutes); // Rotas para os incidentes
app.use('/api/secretaries', secretaryRoutes); // Rotas para as secretárias

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Inicializando o servidor
///////////////////////////////////////////////////////////////////////////////////////////////////////
app.listen(PORT, () => {
  console.log(`Servidor rodando na porta ${PORT}`); // Imprime a mensagem de início do servidor
});

