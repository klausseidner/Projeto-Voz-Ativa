///////////////////////////////////////////////////////////////////////////////////////////////////////
// Importando dependencias necesarias
///////////////////////////////////////////////////////////////////////////////////////////////////////
const mysql = require('mysql2/promise'); // Pool de conexiones para la base de datos

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Configurando a piscina de conexões para o banco de dados
///////////////////////////////////////////////////////////////////////////////////////////////////////
const pool = mysql.createPool({ // Configurando os detalhes da conexão
  host: process.env.DB_HOST, // Hostname do banco de dados
  user: process.env.DB_USER, // Usuário do banco de dados
  password: process.env.DB_PASSWORD, // Senha do banco de dados
  database: process.env.DB_NAME // Nome da base de dados
});

module.exports = pool; // Exportando a piscina de conexões para o módulo

