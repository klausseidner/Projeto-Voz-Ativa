///////////////////////////////////////////////////////////////////////////////////////////////////////
// Importando dependências necessárias
///////////////////////////////////////////////////////////////////////////////////////////////////////
const express = require('express'); // Framework Express
const router = express.Router(); // Roteador do Express
const secretaryController = require('../controllers/secretaryController'); // Importando o controller de Secretaria

///////////////////////////////////////////////////////////////////////////////////////////////////////
// Rotas para Secretaria
///////////////////////////////////////////////////////////////////////////////////////////////////////
router.post('/', secretaryController.createSecretary);  // Cria uma nova Secretaria
router.get('/', secretaryController.getAllSecretaries);  // Lista todas as Secretarias
router.get('/:id', secretaryController.getSecretaryById);  // Busca uma Secretaria específica pelo ID
router.put('/:id', secretaryController.updateSecretary);  // Atualiza uma Secretaria
router.delete('/:id', secretaryController.deleteSecretary); // Exclui uma Secretaria

module.exports = router; // Exporta o roteador da Secretaria

